<?php

namespace AppBundle\Entity;

class TrainCategory {
    /** @var string */
    public $category;

    /**
     * @return string
     */
    public function getCategory(): string
    {
        return $this->category;
    }

    /**
     * @param string $category
     */
    public function setCategory(string $category)
    {
        $this->category = $category;
    }
}